package pl.devmentoring.oop;

public class Zad1 {
    // Wymień i omów główne założenia programowania obiektowego - OOP.

    // Programowanie obiektowe (OOP, od ang. Object-Oriented Programming) to paradygmat programowania,
    // który używa "obiektów" – struktur danych składających się z pól danych i metod – do projektowania aplikacji i
    // programów. OOP koncentruje się na wykorzystaniu obiektów zamiast akcji; programy są organizowane wokół obiektów, nie
    // logiki i funkcji. Oto główne założenia OOP:
    //
    // Abstrakcja:
    //Abstrakcja polega na ukrywaniu skomplikowanych rzeczywistości, podczas gdy pokazuje tylko niezbędne aspekty obiektu.
    // To znaczy, że użytkownik interaktywnie może widzieć tylko to, co jest istotne, podczas gdy szczegóły implementacji są ukryte.
    // Pozwala to na bardziej zrozumiały i czystszy interfejs programistyczny.
    //
    //Hermetyzacja (enkapsulacja):
    //Hermetyzacja to technika używana do ukrywania stanu obiektu w taki sposób, że dane obiektu są ukryte i
    // chronione przed zewnętrznym dostępem. Dostęp do tych danych jest regulowany przez publiczne metody klasy,
    // które często nazywane są getterami i setterami. Enkapsulacja pomaga w ochronie integralności danych oraz w
    // ograniczeniu skutków ubocznych.
    //
    //Dziedziczenie:
    //Dziedziczenie to mechanizm, przez który jedna klasa (nazywana klasą pochodną, czy dzieckiem) może dziedziczyć
    // cechy (pola i metody) innej klasy (nazywanej klasą bazową lub rodzicem). Dzięki temu można reużywać, rozszerzać lub
    // modyfikować istniejące kody, co ułatwia zarządzanie dużymi projektami i poprawia możliwości ponownego użycia kodu.
    //
    //Polimorfizm:
    //Polimorfizm to zdolność do wywoływania różnych metod, które mogą różnić się w zależności od kontekstu lub klasy,
    // które są wywoływane, ale mają tę samą nazwę. W praktyce, dzięki polimorfizmowi, można używać klasy pochodnej jak jej rodzica,
    // ale zachowuje ona możliwość nadpisania pewnych metod w celu dostosowania ich działania do specyficznych potrzeb.
}
