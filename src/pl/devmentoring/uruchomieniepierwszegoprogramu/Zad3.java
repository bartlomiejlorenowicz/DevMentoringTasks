package pl.devmentoring.uruchomieniepierwszegoprogramu;

public class Zad3 {

    // Czym jest: JRE, JVM, JDK oraz jakie są różnice pomiędzy nimi?

    // JRE – Java Runtime Environment, jest częścią oprogramowania niezbędną do uruchamiania aplikacji napisanych w języku Java.  Główne komponenty to:
    //1.	Wirtualna Maszyna Java (JVM): Serce JRE, które odpowiada za uruchomienie skompilowanego kodu bajtowego.
    //2.	Biblioteki Klas Java: Zestaw bibliotek standardowych dostarczonych przez platformę Java, które są wykorzystywane przez różne aplikacje Java.
    //3.	Ładowacz Klas: Class Loader: component odpowiedzialny za ładowanie klas potrzebnych do uruchomienia aplikacji java.
    //4.	Środowisko uruchomieniowe: zawiera niezbędne narzędzia do zarządzania aplikacjami Java, w tym zarządzanie pamięcią, śledzenie wyjątków i zabezpieczenia.
    //
    //JVM – Java Virtual Machine: jest kluczowym elementem technologii java. Jest to silnik, który umożliwia uruchomienie aplikacji napisanych w języku Java na dowolnym urządzeniu bez potrzeby modyfikacji kodu źródłowego, dzięki czemu Java jest znana jako „write once, run anywhere”.
    //Główne aspekty:
    //- niezależność od platformy: kod bajtowy generowany przez kompilator java jest uniwersalny dla wszystkich platform.
    //- zarządzanie pamięcią: posiada garbage collection, który pomaga w usuwaniu nieużywanych obiektów, co zapobiega wyciekom pamięci.
    //- bezpieczeństwo: JVM zapewnia bezpieczeństwo przez piaskownicę (sandbox), w której uruchamiane są aplikacje java, ograniczając ich dostęp do zasobów sieciowych i systemowych.
    //- wydajność: posiada Just-In-Time (JIT) compilation, pozwalają na kompilację kodu bajtowego do kodu maszynowego w locie, co znacząco wpływa na wydajność aplikacji.
    //- ładowanie klas: JVM jest odpowiedzialna za ładowanie, linkowanie, i inicjalizację klas potrzebnych do uruchomienia aplikacji.
    //
    //JDK – Java Development Kit. Zawiera wszystko co jest potrzebne do projektowania, pisania, kompilacji, i testowania programów Java. Jest to zbiór narzędzi programistycznych, które umożliwiają tworzenie oprogramowania na platformie Java.
    //Kluczowe składniki:
    //- kompilator Java,
    //- narzędzia JVM,
    //- Java API,
    //- JavaDoc,
    //- narzędzia do monitorowania diagnostyki,
}
